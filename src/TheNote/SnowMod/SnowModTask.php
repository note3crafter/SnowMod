namespace TheNote\SnowMod;

use pocketmine\scheduler\Task;

class SnowModTask extends Task
{
    function __construct(SnowMod $player)
    {
        $this->player = $player;
    }

    public function onRun(): void
    {
        $player = $this->player;
        $player->SnowMod();
    }
}